//CC 6th variables practice in c

#include <stdio.h>

int main() {
    // Declare and initialize variables of different types
    char name[] = "Charlie Cogswell";
    int luckyNumber = 23;
    float gpa = 2.7;
    char breakfast[] = "Cereal";
    char favoriteColor[] = "pink";
    char schoolName[] = "Ucas";
    int currentYear = 2025;
    char eyeColor[] = "Brown";
    int age = 14;
    char favoriteSubject[] = "study hall";

    // Print each variable with a descriptive label
    printf("Name: %s\n", name);
    printf("Lucky Number (1-10): %d\n", luckyNumber);
    printf("GPA: %.2f\n", gpa);
    printf("Breakfast: %s\n", breakfast);
    printf("Favorite Color: %s\n", favoriteColor);
    printf("School Name: %s\n", schoolName);
    printf("Current Year: %d\n", currentYear);
    printf("Eye Color: %s\n", eyeColor);
    printf("Age: %d\n", age);
    printf("Favorite Subject: %s\n", favoriteSubject);

    return 0;
}
